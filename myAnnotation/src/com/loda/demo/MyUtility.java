public class MyUtility {
    /**
     * 返回两个字符串拼接后的结果
     * @param str1 第一个字符串
     * @param str2 第二个字符串
     * @return 返回拼接后的结果
     */
    public String concat(String str1,String str2) {
        return str1.concat(str2);
    }
}
